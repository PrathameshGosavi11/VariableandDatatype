public class Variable
{   
    public static void main(String[] args)
    {
        int number=10;
        System.out.println("This is a integer type variable:"+number);

        short num=1200;
        System.out.println("This is byte type varibale: "+num);

        long longValue=9000000l;
        System.out.println("Long value is:"+longValue); 

        System.out.println("decimal point ");

        float fValue=12.5f;
        System.out.println("float value is:"+fValue);

        double dValue=12.003d;
        System.out.println("double value is:"+dValue);

        char cValue='f';
        System.out.println("character value is :"+cValue);

        boolean b=false; // local variable initilized then use.
        System.out.println(" value is:"+b);

        
    }
}